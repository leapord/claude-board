let currentLocale = 'zh-CN';

function setLocale(locale) {
  currentLocale = LOCALES[locale] ? locale : 'zh-CN';
}

function t(key) {
  return (LOCALES[currentLocale] && LOCALES[currentLocale][key]) || key;
}

